package com.travonect.api.model.user;

public class UserBasicInfo {
    
    private String physiqueType;
    private long height;
    private String exerciseInfo;
    private String politicalViews;
    private String religiousViews;
    private String drinkingHabits;
    private String smokingHabits;
    private String drugHabits;
    private String hashtags;

    public String getPhysiqueType() {
        return physiqueType;
    }

    public void setPhysiqueType(String physiqueType) {
        this.physiqueType = physiqueType;
    }

    public long getHeight() {
        return height;
    }

    public void setHeight(long height) {
        this.height = height;
    }

    public String getExerciseInfo() {
        return exerciseInfo;
    }

    public void setExerciseInfo(String exerciseInfo) {
        this.exerciseInfo = exerciseInfo;
    }

    public String getPoliticalViews() {
        return politicalViews;
    }

    public void setPoliticalViews(String politicalViews) {
        this.politicalViews = politicalViews;
    }

    public String getReligiousViews() {
        return religiousViews;
    }

    public void setReligiousViews(String religiousViews) {
        this.religiousViews = religiousViews;
    }

    public String getDrinkingHabits() {
        return drinkingHabits;
    }

    public void setDrinkingHabits(String drinkingHabits) {
        this.drinkingHabits = drinkingHabits;
    }

    public String getSmokingHabits() {
        return smokingHabits;
    }

    public void setSmokingHabits(String smokingHabits) {
        this.smokingHabits = smokingHabits;
    }

    public String getDrugHabits() {
        return drugHabits;
    }

    public void setDrugHabits(String drugHabits) {
        this.drugHabits = drugHabits;
    }

    public String getHashtags() {
        return hashtags;
    }

    public void setHashtags(String hashtags) {
        this.hashtags = hashtags;
    }
    
}
